	public int getSpawnGhostY() {
		return spawnGhostY;
	}

	public void setCase(int x, int y, int i) {
	if(type.equals(DEFAULT)) defaultMap[x][y] = i;
	if(type.equals(GOOGLE)) defaultMap[x][y] = i;
	}

	public void setSpawnPacman() {
		int[][] tempMap = getMap();
		for (int i = 0; i < tempMap.length; i++) {
			for (int u = 0; u < tempMap[i].length; u++) {
				if ( tempMap[i][u] == 6) {
					spawnPacmanX = i;
					spawnPacmanY = u;
				}
			}
		}
	}
	
	public void setSpawnGhost() {
		for (int i = 0; i < longueur; i++) {
			for (int u = 0; u < hauteur; u++) {
				if (type.equals(DEFAULT) && defaultMap[i][u] == 7) {
					spawnGhostX = i;
					spawnGhostY = u;
				}
				if (type.equals(GOOGLE) && googleMap[i][u] == 7) {
					spawnGhostX = i;
					spawnGhostY = u;
				}
			}
		}
	}

	public void swapMap() {
		if (type.equals(GOOGLE)) {
			hauteur = 31;
			longueur = 28;
			type = DEFAULT;
			setSpawnPacman();
			setSpawnGhost();
		}
		if (type.equals(DEFAULT)) {
			hauteur = 17;
			longueur = 58;
			type = GOOGLE;
			setSpawnPacman();
			setSpawnGhost();
		}
	}

	

