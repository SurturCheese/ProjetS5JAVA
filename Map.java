	public Map(String type) {
		this.type = type;
		if (type.equals(DEFAULT)) {
			this.hauteur = 31;
			this.longueur = 28;
		}
		if (type.equals(GOOGLE)) {
			this.hauteur = 17;
			this.longueur = 58;
		}
		setSpawnPacman();
		setSpawnGhost();
	}

