	public Map(String type) {
		this.type = type;
		if (type.equals(DEFAULT)) {
			this.hauteur = 31;
			this.longueur = 28;
		}
		if (type.equals("google")) {
			this.hauteur = 17;
			this.longueur = 58;
		}
	}

	public int getHauteur() {
		return hauteur;
	}

	public int getLongueur() {
		return longueur;
	}

	public int[][] getMap() {
		if (type.equals(DEFAULT))
			return defaultMap;
		return googleMap;
	}

	public String getType() {
		return type;
	}

	public void setCase(int x, int y, int i) {
	if(type.equals(DEFAULT)) defaultMap[x][y] = i;
	if(type.equals(GOOGLE)) defaultMap[x][y] = i;
	}

	public void setSpawn() {
		for (int i = 0; i < longueur; i++) {
			for (int u = 0; u < hauteur; u++) {
				if (type.equals(DEFAULT) && defaultMap[i][u] == 6) {
					spawnX = i;
					spawnY = u;
				}
				if (type.equals(GOOGLE) && googleMap[i][u] == 6) {
					spawnX = i;
					spawnY = u;
				}
			}
		}
	}

	public void swapMap() {
		if (type.equals(GOOGLE)) {
			hauteur = 31;
			longueur = 28;
			type = DEFAULT;
			setSpawn();
			// setup le nouveau centre
		}
		if (type.equals(DEFAULT)) {
			hauteur = 17;
			longueur = 58;
			type = GOOGLE;
			setSpawn();
			// setup le nouveau centre
		}
	}

