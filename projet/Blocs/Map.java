	public int[][] getMap() {
		if (type.equals(DEFAULT))
			return defaultMap;
		else
			return googleMap;
	}

	public void setMapInfo() {
		int[][] tempMap = getMap();
		boolean greenPelletUsed = true;
		for (int i = 0; i < tempMap.length; i++) {
			for (int j = 0; j < tempMap[i].length; j++) {
				if (tempMap[i][j] == 6) {
					spawnPacmanX = i;
					spawnPacmanY = j;
				}
				if (tempMap[i][j] == 7) {
					spawnGhostX = i;
					spawnGhostY = j;
				}
				if (tempMap[i][j] == 8 && greenPelletUsed) {
					this.teleportA[0] = i;
					this.teleportA[1] = j;
					greenPelletUsed = false;
				} else if (tempMap[i][j] == 8 && !greenPelletUsed) {
					this.teleportB[0] = i;
					this.teleportB[1] = j;
				}
			}
		}
	}

	public int[] getTeleportPoint(int posX, int posY) {
		if (Arrays.equals(new int[] { posX, posY }, teleportA))
			return teleportB;
		else
			return teleportA;
	}

	public void swapMap() {
		if (type.equals(GOOGLE)) {
			height = defaultMap[0].length;
			length = defaultMap.length;
			type = DEFAULT;
		} else if (type.equals(DEFAULT)) {
			height = googleMap[0].length;
			length = googleMap.length;
			type = GOOGLE;
		}
		setMapInfo();
	}
